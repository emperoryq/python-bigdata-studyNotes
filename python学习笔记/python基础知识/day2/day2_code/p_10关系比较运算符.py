def test_func():
    # ==
    print(1 == 2)
    print(1 == 1)

    print('*' * 10)

    print(1 != 2)
    print(1 != 1)

    print('*' * 10)

    print(1 > 2)
    print(1 > 1)

    print('*' * 10)

    print(1 < 2)
    print(1 < 1)

    print('*' * 10)

test_func()