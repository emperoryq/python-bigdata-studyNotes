'''
组包和拆包
'''

# 组包

a = 1, 2, 3, 4, 5
print(a)
print(type(a))

# 拆包
# 注意：拆包时，容器中的元素个数要和变量个数相同
# a1, a2 ,a3, a4 ,a5 = a[0], a[1], a[2], a[3], a[4]
# a1, a2 ,a3, a4 ,a5 =a
# a1, a2 ,a3, a4 ,a5 =[1, 2, 3, 4 ,5]
# a1, a2 ,a3, a4 ,a5 ='abcde'
a1, a2 ,a3, a4 ,a5 ={'a':1,'b':2,'c':3,'d':4,'e':5}
print(a1, a2, a3, a4, a5)